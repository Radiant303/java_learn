package com.itheima.demo1;

public class IncredyNum {
    public static void main(String[] args) {
        //自增自减运算符
        int a = 10;
        a++;
        System.out.println(a);
        a--;
        System.out.println(a);

        //先用和后用
        int x = 10;
        int y = x++;
        System.out.println(y);

        System.out.println(x);
    }
}
